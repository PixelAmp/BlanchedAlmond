namespace PUBGSharp.Data
{
    /// <summary>
    /// Region to retrieve stats from. For all stats combined, use <see cref="AGG"/>.
    /// </summary>
    public enum Region
    {
        AGG,    // All regions combined
        NA,     // North America
        EU,     // Europe
        AS,     // Asia
        OC,     // Oceania
        SA,     // South America
        SEA,     // South East Asia
        KRJP    // Korea Japan
    }
}
